extension UIColor {
    static var lightBackground: UIColor {
        return UIColor(red: 220/255, green: 220/255, blue: 220/255, alpha: 1)
    }

    static var darkBackground: UIColor {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }

    static var myGiphyRed: UIColor {
        return UIColor(red: 158 / 255, green: 58 / 255, blue: 53 / 255, alpha: 1)
    }

    static var myGiphyBlue: UIColor {
        return UIColor(red: 107 / 255, green: 138 / 255, blue: 145 / 255, alpha: 1)
    }

    static var myGiphyGreen: UIColor {
        return UIColor(red: 166 / 255, green: 182 / 255, blue: 164 / 255, alpha: 1)
    }
}
